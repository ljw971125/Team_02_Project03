package room;

public class RoomDTO {
    // 멤버 변수 선언 - 필드
    private int num;
    private int area;
    private int capacity;
    private int airConditioner;
    private int computer;
    private int whiteboard;
    private String wifi;
    private int refrigerator;
    private String image;
    private int price;
    
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getArea() {
		return area;
	}
	public void setArea(int area) {
		this.area = area;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public int getAirConditioner() {
		return airConditioner;
	}
	public void setAirConditioner(int airConditioner) {
		this.airConditioner = airConditioner;
	}
	public int getComputer() {
		return computer;
	}
	public void setComputer(int computer) {
		this.computer = computer;
	}
	public int getWhiteboard() {
		return whiteboard;
	}
	public void setWhiteboard(int whiteboard) {
		this.whiteboard = whiteboard;
	}
	public String getWifi() {
		return wifi;
	}
	public void setWifi(String wifi) {
		this.wifi = wifi;
	}
	public int getRefrigerator() {
		return refrigerator;
	}
	public void setRefrigerator(int refrigerator) {
		this.refrigerator = refrigerator;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
    
}